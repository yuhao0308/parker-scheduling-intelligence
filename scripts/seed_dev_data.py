TIMECLOCK_CSV_PATH = (
    Path(__file__).resolve().parents[1]
    / "artifacts"
    / "timeclock"
    / "pay_period_summary.csv"
)


def _load_timeclock_records() -> list[dict[str, object]]:
    """Convert pay_period_summary.csv rows into HoursLedger sync records.

    Worked hours = REG + OT + DT + HOL (matches
    ``app.services.timeclock_ingest.summary_to_ledger_record`` — kept in sync
    so seed-time and runtime ingest produce identical ledger state). PTO and
    SICK are deliberately excluded because the workload bar shows time on
    the floor, not total paid time.
    """
    if not TIMECLOCK_CSV_PATH.exists():
        return []
    records: list[dict[str, object]] = []
    with TIMECLOCK_CSV_PATH.open(newline="") as f:
        for row in csv.DictReader(f):
            worked = (
                float(row["regular_hours"])
                + float(row["overtime_hours"])
                + float(row["doubletime_hours"])
                + float(row["holiday_hours"])
            )
            records.append(
                {
                    "employee_id": row["person_number"],
                    "cycle_start_date": row["pay_period_start"],
                    "hours_this_cycle": round(worked, 2),
                    "shift_count_this_biweek": int(row["shift_count"]),
                }
            )
    return records


def seed_hours():
    """Seed hours ledger for the current pay cycle.

    Two-phase seed so the workload monitor reads correctly for any view
    month:

      1. Post the curated BASE_HOURS first — these establish the seed
         "current cycle" for the ~25 hand-named employees and create
         zero-hour placeholders for everyone else.
      2. Post every (employee, cycle) pair from the time-clock CSV —
         covers all 214 employees across all 8 biweekly cycles in the
         dummy window (Jan 19 -> Apr 27 2026). Upsert semantics mean
         the CSV values overwrite step 1 wherever they overlap.

    If the CSV is missing (the user hasn't run generate_timeclock_data.py
    yet), step 2 is skipped with a warning instead of failing.
    """
    base_records = build_hours_records()
    resp = httpx.post(f"{BASE_URL}/sync/hours", json={"records": base_records})
    _print_response("Hours sync (base)", resp)

    timeclock_records = _load_timeclock_records()
    if not timeclock_records:
        print(
            "  [warn] artifacts/timeclock/pay_period_summary.csv not found; "
            "skipping time-clock ingest. Run "
            "`python scripts/generate_timeclock_data.py` then re-run this "
            "seed script to populate the workload monitor with realistic "
            "worked hours."
        )
        return

    # Chunk the upload + bump the timeout. ``sync_hours`` does an N+1
    # SELECT per record (no bulk upsert), so 1712 rows in a single request
    # can easily blow past the default 5-second httpx timeout. Chunks of
    # ~200 keep each request comfortably under a second and let us see
    # progress in the seed log.
    chunk_size = 200
    timeout = 60.0
    total_created = 0
    total_updated = 0
    for offset in range(0, len(timeclock_records), chunk_size):
        chunk = timeclock_records[offset : offset + chunk_size]
        resp = httpx.post(
            f"{BASE_URL}/sync/hours",
            json={"records": chunk},
            timeout=timeout,
        )
        try:
            payload = resp.json()
            total_created += payload.get("created", 0)
            total_updated += payload.get("updated", 0)
        except ValueError:
            _print_response(
                f"Hours sync (time-clock chunk @ {offset})", resp
            )
            return
    print(
        f"Hours sync (time-clock CSV): {len(timeclock_records)} rows in "
        f"{(len(timeclock_records) + chunk_size - 1) // chunk_size} chunks "
        f"— created={total_created} updated={total_updated}"
    )


