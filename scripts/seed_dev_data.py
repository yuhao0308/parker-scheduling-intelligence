def _make_staff_record(
    employee_id: str,
    name: str,
    license: str,
    employment_class: str,
    home_unit_id: str,
    hire_date: str,
    cross_trained_units: list[str] | None = None,
    zip_code: str | None = None,
) -> dict[str, object]:
    return {
        "employee_id": employee_id,
        "name": name,
        "license": license,
        "employment_class": employment_class,
        "zip_code": zip_code or UNIT_ZIP_CODES[home_unit_id],
        "home_unit_id": home_unit_id,
        "cross_trained_units": cross_trained_units or [],
        "hire_date": hire_date,
    }


def _generated_name(sequence: int) -> str:
    first = GENERATED_FIRST_NAMES[sequence % len(GENERATED_FIRST_NAMES)]
    last = GENERATED_LAST_NAMES[
        (sequence // len(GENERATED_FIRST_NAMES)) % len(GENERATED_LAST_NAMES)
    ]
    return f"{first} {last}"


def _generated_hire_date(sequence: int) -> str:
    hire_year = 2016 + (sequence % 9)
    hire_month = (sequence % 12) + 1
    hire_day = ((sequence * 3) % 28) + 1
    return f"{hire_year:04d}-{hire_month:02d}-{hire_day:02d}"


def _generated_cross_training(unit_id: str, sequence: int) -> list[str]:
    buddy = CROSS_TRAIN_BUDDIES.get(unit_id)
    if not buddy or sequence % 2:
        return []
    return [buddy]


def build_staff_records() -> list[dict[str, object]]:
    staff = list(BASE_STAFF)
    name_sequence = 0
    next_rn = 7
    next_cna = 16

    for unit_id, count in ADDITIONAL_RN_BY_UNIT.items():
        for unit_sequence in range(count):
            staff.append(
                _make_staff_record(
                    employee_id=f"RN{next_rn:03d}",
                    name=_generated_name(name_sequence),
                    license="RN",
                    employment_class="FT",
                    home_unit_id=unit_id,
                    hire_date=_generated_hire_date(name_sequence),
                    cross_trained_units=_generated_cross_training(
                        unit_id, unit_sequence
                    ),
                )
            )
            next_rn += 1
            name_sequence += 1

    for unit_id, count in ADDITIONAL_CNA_BY_UNIT.items():
        for unit_sequence in range(count):
            staff.append(
                _make_staff_record(
                    employee_id=f"CNA{next_cna:03d}",
                    name=_generated_name(name_sequence),
                    license="CNA",
                    employment_class="FT",
                    home_unit_id=unit_id,
                    hire_date=_generated_hire_date(name_sequence),
                    cross_trained_units=_generated_cross_training(
                        unit_id, unit_sequence
                    ),
                )
            )
            next_cna += 1
            name_sequence += 1

    if len(staff) != FULL_STAFF_HEADCOUNT:
        raise ValueError(
            f"Expected {FULL_STAFF_HEADCOUNT} seeded staff records, got {len(staff)}"
        )
    return staff


def build_hours_records() -> list[dict[str, object]]:
    records = list(BASE_HOURS)
    existing_employee_ids = {record["employee_id"] for record in records}

    for staff in build_staff_records():
        employee_id = str(staff["employee_id"])
        if employee_id in existing_employee_ids:
            continue
        cycle_start_date = "2026-03-30" if staff["license"] == "RN" else "2026-04-06"
        records.append(
            {
                "employee_id": employee_id,
                "cycle_start_date": cycle_start_date,
                "hours_this_cycle": 0.0,
                "shift_count_this_biweek": 0,
            }
        )

    return records


