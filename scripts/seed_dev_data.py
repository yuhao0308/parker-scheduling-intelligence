def _print_response(label: str, resp: httpx.Response) -> None:
    """Safe response printer — falls back to raw text when JSON parse fails.

    The bulk-sync endpoints occasionally return an empty body or an HTML
    error page (FastAPI default handler). The original ``resp.json()`` call
    crashed mid-print with no clue what the server actually said. This
    helper prints the parsed JSON when it can, otherwise the status code
    plus the raw text + content-type so the failure is debuggable.
    """
    try:
        parsed = resp.json()
    except ValueError:
        body_preview = resp.text[:500] if resp.text else "<empty body>"
        ctype = resp.headers.get("content-type", "?")
        print(
            f"{label}: {resp.status_code} [{ctype}] non-JSON response — "
            f"{body_preview!r}"
        )
        return
    print(f"{label}: {resp.status_code} — {parsed}")

